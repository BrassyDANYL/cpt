         <h4>RAT: jail case management, planning, and supervision</h4>
         <p>
         In jails, RATs are used to supplement case management, a process intended to reduce risk by ensuring individuals have
         access to needed resources. Case management refers to the process in which officials evaluate incarcerated peoples’
         needs and risks, and connect them to appropriate services and programs. Intended to provide continuity of care between
         incarceration and the community, case management considers factors related to physical and mental health, substance
         abuse, education, housing, and employment.</p>
         <p>
         In this context RATs serve to automate the evaluation and resource recommendation functions. They’re used to funnel
         scarce resources—attention and programming from case managers—towards the supposedly highest risk individuals. These
         tools are not only diagnostic but distributive as well; they are meant to provide case managers with recommendations
         about how to best reduce individuals’ risks based on their evaluated needs.</p>
