         <h4>Basics</h4>
         <p>
         Face recognition technology, (often called “facial” recognition), is an algorithmic biometric tool that which compares
         two face images to determine the likelihood they are a match. Law enforcement purportedly uses face recognition to
         produce leads when trying to identify people in photos or video footage. There are generally two types of face
         recognition: one-to-one matching, which compares two photos to one another to verify they’re the same person; and
         one-to-many matching, which compares one photo to a large database in order to identify someone. In the latter, officers
         upload a “probe” photo of a face in question, and an algorithm analyzes its features, compares it to a database of face
         photos, and then issues a numerical score reflecting the similarity of the “probe” photo face to other faces in the
         database.</p>
         <p>
         When analyzing a photo, the algorithm first detects the presence of a face. Then, it “normalizes” the image, scaling and
         rotating so it’s in a standard position for comparison. Next, it extracts facial features like eyes and nose from the
         image. Finally, it compares these features to those in another photo, assigning a score based on how similar they are.
         Face recognition refers to this specific process, as distinct from other face analysis tools that purport to classify
         people’s faces by factors like gender or age.</p>
         
