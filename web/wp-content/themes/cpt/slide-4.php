         <h4>Basics</h4>
         <p>“Crime forecasting” refers to two separate but related processes: predictive policing and data-driven prosecution. Both
         processes use historical crime data to make predictions about future crime, using these predictions to inform who is
         policed and prosecuted. Based on existing “hot spot” theories of policing, crime forecasting assumes there are
         particular areas and individuals who are more “at-risk” for criminal activity. Automated tools analyze data such as
         where past crimes occurred and features of the local environment to determine if a location is “at-risk,” or if an
         individual is “likely” to commit or be the victim of a crime. Law enforcement sees these tools as more efficient and
         less biased than humans, capable of making quick decisions while also cutting costs.</p>
         
