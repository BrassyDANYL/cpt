         <h4>Risk assessment tools (RATs)</h4>
         <p>
         A defendant often has their first encounter with an RAT before trial when their bail is being set. Here, RATs are used
         in one of two ways: to gauge the likelihood of an individual’s failure to appear (FTA) at their trial, or to assess
         their likelihood of committing another crime before trial. Historically, bail was used exclusively as a means to ensure
         court appearances and discourage flight. However, due to a number of political shifts, the concept of an individual’s
         “dangerousness” to the community became a major factor in bail determination. RATs, when used to set bail, now score
         people based on their FTA likelihood and risk of committing a crime after pretrial release.</p>
         <p>
         Initially, some critics of mass incarceration supported this application of RATs as a way to decrease pretrial detention
         rates and eliminate the disadvantage poor people face with cash bail. But these tools led to only small increases in
         pretrial release, and, in fact, increased FTAs and pretrial crime rates. Moreover, they yet again exhibit the
         limitations of data-driven technologies: because RATs are trained on specific data, they fail to reflect the true
         complex realities of the criminal legal system. Across place and time, RATs apply a one-size-fits-all approach to
         different contexts not necessarily compatible with the originally collected data. For example, RATs are often trained on
         older data; but if a jurisdiction has since instituted pretrial reforms, the RAT will reflect outdated practices rather
         than current ones. If a new diversion program is introduced to decrease pretrial detention and recidivism, the RAT does
         not take this into account, hiding any potential effects the reform has and reinforcing historical trends.
         </p>
