         <h4>Risk assessment tools (RATs)</h4>
         <p>
         Risk assessment tools (RATs) are a general class of algorithms that purport to predict the likelihood of an individual
         engaging in certain unwanted behavior in the future. Most commonly in the criminal legal system, they are used to
         forecast future misconduct; specifically, the chance an individual will skip bail, commit another crime, or be
         rearrested. They are also used to anticipate challenges incarcerated people will have during transitions into and out of
         institutionalization.</p>
         <p>
         With “risk” in mind, criminal legal system officials make decisions about peoples’ liberty, supervision, and
         criminogenic needs. There are six main stages at which RATs are employed: pretrial, jail, prison, release, parole, and
         probation. Most notably, RATs are used for setting bail, case management in jails, sentencing, case management in
         prisons, informing discretionary release/parole, case management during parole, and determining probation. However, they
         can theoretically be used at any point during an individual’s interaction with the criminal legal system.</p>
         
